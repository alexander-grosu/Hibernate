public class Primitive {

    public static void main(String[] args) {
        System.out.println("Integer maxValue: " + Integer.MAX_VALUE);
        System.out.println("Integer minValue: " + Integer.MIN_VALUE);
        System.out.println("Byte maxValue: " + Byte.MAX_VALUE);
        System.out.println("Byte minValue: " + Byte.MIN_VALUE);
        System.out.println("Short maxValue: " + Short.MAX_VALUE);
        System.out.println("Short minValue: " + Short.MIN_VALUE);
        System.out.println("Long maxValue: " + Long.MAX_VALUE);
        System.out.println("Long minValue: " + Long.MIN_VALUE);
        System.out.println("Double maxValue: " + Double.MAX_VALUE);
        System.out.println("Double minValue: " + -Double.MAX_VALUE);
        System.out.println("Float maxValue: " + Float.MAX_VALUE);
        System.out.println("Float minValue: " + -Float.MAX_VALUE);
        
    }
}
